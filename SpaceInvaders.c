#include "TExaS.h"
#include "GameEngine.h"

void DisableInterrupts(void); // Disable interrupts
void EnableInterrupts(void);  // Enable interrupts


unsigned long Semaphore=0;

int main(void)
{
	TExaS_Init(SSI0_Real_Nokia5110_Scope);			// work at 80MHZ
	Game_Init();																// initalize the game inputs & outputs & init spirts 
	EnableInterrupts();													// Making peroidic interrrupt at 30 HZ 
	while(1)
	{
		if(Semaphore==1)
		{
			Semaphore=0;										// clear semaphore 
			Game_Controls();								// check inputs
			Game_Engine();									// game algorthim 	
			Update_Screen();								// put new screen 
		}
	}
}


